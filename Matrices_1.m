%Creación de una matriz A
A = [1 1 1; 3 5 4; 3 6 5]

%Obtener el determinante de una matriz A
Determinante= det(A)

%Obtener la matriz transpuesta de una matriz A
M_transpuesta= transpose(A)

%Obtener la matriz inversa de una matriz A
M_inversa= inv(A)

%Multiplicación de la matriz A y su matriz inversa 
Identidad= A*inv(A)